public class assisgnmentOperator {
    public static void main(String[] args) {
        //assignment operator: this operator are assign values to variable
        //Compound assignment operator

        //+=
        int a = 10;
        a+=2;
        System.out.println(a);

        //-=
        int alldays = 30;
        alldays -= 22;
        System.out.println(alldays);

        //*=
        int mysal = 2000;
        mysal *= 2;
        System.out.println(mysal);

        // /=
        int peoples = 200;
        peoples /= 2;
        System.out.println(peoples);

        // %=
        int A = 10;
        A %= 2;
        System.out.println(A);
    }

}
